import { readFile, writeFile } from "node:fs/promises";

const data = JSON.parse(await readFile("model/localRecipeTrainingData.json", "utf8"));

const ingredientFeatureHits = {
  measured: 0,
  listed: 0,
  multiWord: 0,
  contextUse: 0,
  descriptor: 0,
  measurementNearby: 0,
  termMention: 0,
  negative: 0
};

const instructionFeatureHits = {
  action: 0,
  prep: 0,
  ingredient: 0,
  timing: 0,
  sequence: 0,
  negative: 0
};

const totals = {
  ingredients: 0,
  steps: 0,
  negatives: 0
};

for (const example of data.examples) {
  const transcript = example.transcript.toLowerCase();
  totals.ingredients += example.ingredients.length;
  totals.steps += example.steps.length;
  totals.negatives += example.negativeTerms.length;

  for (const ingredient of example.ingredients) {
    const featureSet = ingredientFeatures(ingredient, transcript);
    for (const feature of featureSet) ingredientFeatureHits[feature] += 1;
  }

  for (const step of example.steps) {
    const featureSet = instructionFeatures(step, transcript, example.ingredients);
    for (const feature of featureSet) instructionFeatureHits[feature] += 1;
  }

  for (const negative of example.negativeTerms) {
    if (transcript.includes(negative)) {
      ingredientFeatureHits.negative += 1;
      instructionFeatureHits.negative += 1;
    }
  }
}

const weights = {
  version: `${data.version}.${data.examples.length}`,
  thresholds: {
    ingredientMinScore: 2,
    instructionMinScore: 4
  },
  ingredient: {
    measured: weight(ingredientFeatureHits.measured, totals.ingredients, 5, 7),
    listed: weight(ingredientFeatureHits.listed, totals.ingredients, 4, 6),
    multiWord: weight(ingredientFeatureHits.multiWord, totals.ingredients, 2, 4),
    contextUse: weight(ingredientFeatureHits.contextUse, totals.ingredients, 1, 3),
    descriptor: weight(ingredientFeatureHits.descriptor, totals.ingredients, 1, 2),
    measurementNearby: weight(ingredientFeatureHits.measurementNearby, totals.ingredients, 1, 3),
    termMention: weight(ingredientFeatureHits.termMention, totals.ingredients, 2, 3),
    negative: -3
  },
  instruction: {
    action: weight(instructionFeatureHits.action, totals.steps, 4, 6),
    prep: weight(instructionFeatureHits.prep, totals.steps, 1, 3),
    ingredient: weight(instructionFeatureHits.ingredient, totals.steps, 1, 3),
    timing: weight(instructionFeatureHits.timing, totals.steps, 1, 3),
    sequence: weight(instructionFeatureHits.sequence, totals.steps, 1, 2),
    negative: -6
  },
  metadata: {
    trainedExamples: data.examples.length,
    ingredientLabels: totals.ingredients,
    stepLabels: totals.steps
  }
};

const output = `// Generated by scripts/train-local-model.mjs. Do not edit by hand.
export const localRecipeModelWeights = ${JSON.stringify(weights, null, 2)} as const;
`;

await writeFile("src/localRecipeModelWeights.ts", output);

function ingredientFeatures(ingredient, transcript) {
  const features = new Set(["termMention"]);
  if (/\d|cup|tbsp|tsp|gram|ounce|pound|clove/.test(ingredient)) features.add("measured");
  if (/\b(use|using|need|grab|take)\b/.test(transcript)) features.add("listed");
  if (ingredient.includes(" ")) features.add("multiWord");
  if (new RegExp(`\\b(add|season|mix|stir|with|use|using|need|grab|take)\\b[^.]*\\b${escapeRegExp(ingredient)}\\b|\\b${escapeRegExp(ingredient)}\\b[^.]*\\b(add|season|mix|stir|with)\\b`, "i").test(transcript)) features.add("contextUse");
  if (/\b(fresh|ground|chopped|minced|sliced|diced|melted|softened|black|white|brown)\b/.test(transcript)) features.add("descriptor");
  if (/\b\d+\s*(cups?|tbsp|tsp|grams?|g|kg|ml|oz|pounds?|lbs?|cloves?)\b/.test(transcript)) features.add("measurementNearby");
  return features;
}

function instructionFeatures(step, transcript, ingredients) {
  const features = new Set(["action"]);
  if (/\b(pan|pot|oven|heat|medium|high|low|until|golden|tender|brown)\b/.test(transcript)) features.add("prep");
  if (ingredients.some((ingredient) => transcript.includes(ingredient))) features.add("ingredient");
  if (/\b\d+\s*(minutes?|seconds?|degrees?)\b|\b(thirty|five|two|ten)\s+(minutes?|seconds?)\b/.test(transcript)) features.add("timing");
  if (/\b(first|next|then|finally|when)\b/.test(transcript)) features.add("sequence");
  return features;
}

function weight(hits, total, min, max) {
  if (!total) return min;
  const ratio = hits / total;
  return Math.max(min, Math.min(max, Math.round(min + ratio * (max - min))));
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
